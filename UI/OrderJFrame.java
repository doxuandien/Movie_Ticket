    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        // TODO add your handling code here:
        Date date = new Date();
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String strDate = dateFormat.format(date);
        int count = 0;

        Float money = Float.parseFloat(moneyTxt.getText());
        count = chairList.size();
        for (int i = 0; i < chairList.size(); i++) {
            String seat = chairList.get(i);
            Ticket ticket = new Ticket(LoginJFrame.getUsers().getId(), sch_id, seat, money/count, strDate);
            Modify.TicketModify.insertTicket(ticket);
        }
        JOptionPane.showMessageDialog(rootPane, "Booking Successfully");
        
        backBtnActionPerformed(evt); 
    }//GEN-LAST:event_confirmBtnActionPerformed

