    public OrderJFrame(int id, List<String> seatList, float money, String image){
        initComponents();
        this.setLocationRelativeTo(null);
        sch_id = id;
        scheduleList = Modify.ScheduleModify.getSchedulebyID(id);
        chairList = new ArrayList<>();
        for(Schedule sch : scheduleList){
            titleTxt.setText(sch.getMovie_id().getTitle());
            timeTxt.setText(sch.getTime_id().getTime());
            roomTxt.setText(sch.getRoom_id().getRoom());
            dateTxt.setText(sch.getDate_schedule());
            titleM = sch.getMovie_id().getTitle();
        }
        
        for (String seat : seatList){
            chairList.add(seat);
            String c = "";
            for(String e : chairList){
                c += e + " ";
            }
            seatTxt.setText(c);
        }
        
        moneyTxt.setText(money +"");
        Utility.setImageLabel(imageLabel, image);
        img = image;
    }

