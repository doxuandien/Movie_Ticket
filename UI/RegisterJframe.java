    private boolean checkValidate() {
        try {
            
      
        if (fullnameTxt.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Invalid FullName");
            return false;
        }
        if (EmailTxt.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Mời nhập Email!", "Message Dialog", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        if (!EmailTxt.getText().matches("^[_A-Za-z0-9-]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$")) {
            JOptionPane.showMessageDialog(null, "Email không hợp lệ!", "Message Dialog", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        if (PhoneTxt.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập Phone");
            return false;
        }
        if (!PhoneTxt.getText().trim().matches("^\\d{10}$")) {
            JOptionPane.showMessageDialog(null, "Phone gồm 10 số", "Mesage Dialog", JOptionPane.INFORMATION_MESSAGE);
            PhoneTxt.requestFocus();
            return false;
        }

        if (addressTxt.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập Address");
            return false;
        }

        if (passwordTxt.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập Password");
            return false;
        }
 
        if (BirthdayTxt.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập Ngày sinh");
            return false;
        }
            boolean check = false;
            Connection conn = null;
            PreparedStatement preparedStatement = null;
            conn = DriverManager.getConnection(Config.DB_URL, Config.USERNAME, Config.PASSWORD);
            String sql = "SELECT email FROM user Where email = ? ";
            preparedStatement = conn.prepareStatement(sql);
            preparedStatement.setString(1, EmailTxt.getText());
            ResultSet resultSet = preparedStatement.executeQuery();
            if (resultSet.next()) {
                resultSet.close();
                preparedStatement.close();

                check = true;
            } else {
                resultSet.close();
                preparedStatement.close();

                check = false;
            }
            if (check) {
                JOptionPane.showMessageDialog(null, "Email không được trùng!", "Message Dialog", JOptionPane.INFORMATION_MESSAGE);
                return false;
            }
        } catch (SQLException ex) {
             Logger.getLogger(RegisterJframe.class.getName()).log(Level.SEVERE, null, ex);
        }
        return true;
    
        
    }
