    private void RegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterBtnActionPerformed
        // TODO add your handling code here:
        if (!checkValidate()) {
            return;
        }
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        
        String fullname = fullnameTxt.getText();
        String gender = genderBox.getSelectedItem().toString();
        String email = EmailTxt.getText();
        String phone_number = PhoneTxt.getText();
        String address = addressTxt.getText();
        String birthday = sdf.format(BirthdayTxt.getDate());
        String password = passwordTxt.getText();
        
        Date date = new Date();  
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss");  
        String strDate = dateFormat.format(date);  
        String cfpassword = cfpasswordTxt.getText();
        if (!password.equals(cfpassword)) {
            JOptionPane.showMessageDialog(rootPane, "Do not duplicate passwords!!!!");
            return;
        }
        
        MD5 = new Utility(password);
        
        Users users = new Users(fullname, gender, email, phone_number, address, birthday, MD5.getSecurityMD5(), 1, 0, strDate, strDate);
        UserModify.insert(users);
        
        LoginBtnActionPerformed(null);
    }//GEN-LAST:event_RegisterBtnActionPerformed

